// Automatically restore custom user-edited translation overrides from localStorage on module load
if (typeof window !== 'undefined') {
  try {
    const savedAr = localStorage.getItem('shatibha_modified_ar');
    const savedEn = localStorage.getItem('shatibha_modified_en');
    if (savedAr) {
      const parsedAr = JSON.parse(savedAr);
      for (const k in parsedAr) {
        if (k in i18n.ar) {
          (i18n.ar as any)[k] = parsedAr[k];
        }
      }
    }
    if (savedEn) {
      const parsedEn = JSON.parse(savedEn);
      for (const k in parsedEn) {
        if (k in i18n.en) {
          (i18n.en as any)[k] = parsedEn[k];
        }
      }
    }
  } catch (e) {
    console.warn('Failed to load custom local storage translations:', e);
  }
}

export function getTranslation(key: keyof typeof i18n.ar, lang: Language): string {
  return i18n[lang][key] || i18n.ar[key] || String(key);
}
